package myjava;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println("My name is \n Shamima Nasrin");
        System.out.println("I live in \nJackson Heights");
        System.out.println("My cell:\n222333444");
        System.out.println("\tHello Friends");
        System.out.println("We are learning \nJava Programming");
        System.out.println("222         \n    4444");





    }









}
